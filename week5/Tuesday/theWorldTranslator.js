function helloWorld(es) {
    if(es=='es'){
        console.log('hola')
    }
    else if(es=='en'){
        console.log('Hello World');
    }
    else if(es=='de'){
        console.log('hello Welt');
    }
    else{
        console.log('Hello World');
    }
}
helloWorld('en');
helloWorld('de');
helloWorld('es');
helloWorld('together')
